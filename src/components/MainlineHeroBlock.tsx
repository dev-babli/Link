import React from "react";
import Image from "next/image";
import Link from "next/link";

import { ArrowRight } from "lucide-react";

import { MainlineDashedLine } from "./MainlineDashedLine";
import { Button } from "@/components/ui/button";

interface HeroFeature {
  title: string;
  description: string;
  icon?: React.ComponentType<{ className?: string }>;
}

interface MainlineHeroBlockProps {
  title: string;
  description: string;
  heroImage?: string;
  primaryCTA: {
    text: string;
    href: string;
  };
  secondaryCTA?: {
    text: string;
    href: string;
  };
  features: HeroFeature[];
}

export const MainlineHeroBlock = ({
  title,
  description,
  heroImage,
  primaryCTA,
  secondaryCTA,
  features,
}: MainlineHeroBlockProps) => {
  return (
    <section className="py-28 lg:py-32 lg:pt-44">
      <div className="container flex flex-col justify-between gap-8 md:gap-14 lg:flex-row lg:gap-20">
        {/* Left side - Main content */}
        <div className="flex-1">
          <h1 className="text-foreground max-w-160 text-3xl tracking-tight md:text-4xl lg:text-5xl xl:whitespace-nowrap">
            {title}
          </h1>

          <p className="text-muted-foreground text-1xl mt-5 md:text-3xl">
            {description}
          </p>

          <div className="mt-8 flex flex-wrap items-center gap-4 lg:flex-nowrap">
            <Button asChild>
              <Link href={primaryCTA.href}>
                {primaryCTA.text}
              </Link>
            </Button>
            {secondaryCTA && (
              <Button
                variant="outline"
                className="from-background h-auto gap-2 bg-linear-to-r to-transparent shadow-md"
                asChild
              >
                <Link
                  href={secondaryCTA.href}
                  className="max-w-56 truncate text-start md:max-w-none"
                >
                  {secondaryCTA.text}
                  <ArrowRight className="stroke-3" />
                </Link>
              </Button>
            )}
          </div>
        </div>

        {/* Right side - Features */}
        <div className="relative flex flex-1 flex-col justify-center space-y-5 max-lg:pt-10 lg:pl-10">
          <MainlineDashedLine
            orientation="vertical"
            className="absolute top-0 left-0 max-lg:hidden"
          />
          <MainlineDashedLine
            orientation="horizontal"
            className="absolute top-0 lg:hidden"
          />
          {features.map((feature) => {
            const Icon = feature.icon || ArrowRight;
            return (
              <div key={feature.title} className="flex gap-2.5 lg:gap-5">
                <Icon className="text-foreground mt-1 size-4 shrink-0 lg:size-5" />
                <div>
                  <h2 className="font-text text-foreground font-semibold">
                    {feature.title}
                  </h2>
                  <p className="text-muted-foreground max-w-76 text-sm">
                    {feature.description}
                  </p>
                </div>
              </div>
            );
          })}
        </div>
      </div>

      {heroImage && (
        <div className="mt-12 max-lg:ml-6 max-lg:h-[550px] max-lg:overflow-hidden md:mt-20 lg:container lg:mt-24">
          <div className="relative h-[793px] w-full">
            <Image
              src={heroImage.startsWith("/") ? heroImage : `/mainline_service_template/${heroImage}`}
              alt={title}
              fill
              className="rounded-2xl object-cover object-left-top shadow-lg max-lg:rounded-tr-none"
            />
          </div>
        </div>
      )}
    </section>
  );
};

